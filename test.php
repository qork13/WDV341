<p>Version Control Software: software that allows commits and pushes to be able to track changes to the code</p>
<p>Add: Add files to local repo for use</p>
<p>Commit: Commit local code changes to the file in the local repo</p>
<p>Push: Push local code commits to the github repository to update the code in the repo</p>
<p>Pull: Pull changes from remote repo into local repo</p>
<p>Clone: Clone remote repo .git file to be able to create a copy on your local station</p>


